# Dictionary (dist) is a collection of data type which store the data in the form of key & value

Student ={
    'name': 'shubham',
    'age': 22,
    'city': 'varanasi'
}
print(type(Student))

# Print the keys in dist
print(Student.keys())

# Print the values in dist
print(Student.values())

# Print the items in dist
print(Student.items())
